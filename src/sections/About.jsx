import { useRef } from "react";
import AnimatedHeaderSection from "../components/AnimatedHeaderSection";
import { AnimatedTextLines } from "../components/AnimatedTextLines";
import { useGSAP } from "@gsap/react";
import gsap from "gsap";
import petermina from "../../public/images/me.jpeg"
const About = () => {
  const text = `Passionate about AI & the web
    I build intelligent, high-performance solutions
    from prototype to production`;
  const aboutText = `I am Peter Mina — a passionate AI Engineer and Full Stack Developer with expertise in building intelligent web applications and cutting-edge AI solutions.
With hands-on experience in React.js, Node.js, and advanced AI frameworks like PyTorch and TensorFlow, I specialize in integrating machine learning capabilities into real-world applications.
Currently working as a Jr. Full Stack Developer at e-SKY.ca, I joined the Alex Eagles team where we developed autonomous drone systems achieving 96% accuracy, contributed to pharmaceutical research through deep learning drug interaction predictions, and was recognized as a NASA Space Apps 2024 Global Nominee for creating an innovative exoplanet exploration platform.`;
  const imgRef = useRef(null);
  useGSAP(() => {
    gsap.to("#about", {
      scale: 0.95,
      scrollTrigger: {
        trigger: "#about",
        start: "bottom 80%",
        end: "bottom 20%",
        scrub: true,
        markers: false,
      },
      ease: "power1.inOut",
    });

    gsap.set(imgRef.current, {
      clipPath: "polygon(0 100%, 100% 100%, 100% 100%, 0% 100%)",
    });
    gsap.to(imgRef.current, {
      clipPath: "polygon(0% 0%, 100% 0%, 100% 100%, 0% 100%)",
      duration: 2,
      ease: "power4.out",
      scrollTrigger: { trigger: imgRef.current },
    });
  });
  return (
    <section id="about" className="min-h-screen bg-black rounded-b-4xl">
      <AnimatedHeaderSection
        subTitle={"Cod with purpose, Built to scale"}
        title={"About"}
        text={text}
        textColor={"text-white"}
        withScrollTrigger={true}
      />
      <div className="flex flex-col items-center justify-between gap-16 px-10 pb-16 text-xl font-light tracking-wide lg:flex-row md:text-2xl lg:text-3xl text-white/60">
        <img
          ref={imgRef}
          src={petermina}
          alt="peter mina"
          className="w-md rounded-3xl"
        />
        <AnimatedTextLines text={aboutText} className={"w-full"} />
      </div>
    </section>
  );
};

export default About;
